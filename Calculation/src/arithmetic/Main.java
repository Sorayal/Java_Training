package arithmetic;

public class Main {
    public static void main(String[] args) {
        // Plus +
        // Divide /
        // Multiply *
        // Subtract -
        // Remainder of division/ Modulo %
        System.out.println(7 + 3);
        System.out.println(4 / 2);
        System.out.println(3 * 9);
        System.out.println(6 - 11);
        // Gives a int value as result because it will be treated as two int values
        // and won´t convert to floating point on it´s own.
        System.out.println(7 / 3);

        // one floating point value gives a floating point result
        System.out.println(9 / 4.0);
    }
}
