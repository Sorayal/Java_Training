package math.addition;

public class App {
    public static void main(String[] args) {
        int cats = 7;
        int dogs = 5;
        int snakes = 2;

        int total = cats + dogs + snakes;
        System.out.println("Number of cats: " + cats);
        System.out.println("Number of dogs: " + dogs);
        System.out.println("Number of snakes: " + snakes);
        System.out.println("Total number of animals: " + total);
    }
}
