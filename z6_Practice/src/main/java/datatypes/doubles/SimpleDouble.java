package datatypes.doubles;

public class SimpleDouble {
    public static void main(String[] args) {
        // Literal (hardcoded) variable
        double height = 1.8;
        System.out.println("Height is: " + height);

        // you need to put an f to indicate that it is a float value
        float width = 1.6f;
        System.out.println("Width is: " + width);
    }
}
